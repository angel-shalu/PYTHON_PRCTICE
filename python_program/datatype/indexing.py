# String Indexing
my_string = "Python"
print(my_string[0])   # First character → 'P'
print(my_string[3])   # Fourth character → 'h'
print(my_string[-1])  # Last character → 'n'
print(my_string[-3])  # Third from end → 'h'


# List Indexing
my_list = [10, 20, 30, 40, 50]
print(my_list[1])   # Second element → 20
print(my_list[4])   # Fifth element → 50
print(my_list[-2])  # Second last element → 40


# Tuple Indexing
my_tuple = (100, 200, 300, 400)
print(my_tuple[2])   # Third element → 300
print(my_tuple[-1])  # Last element → 400
